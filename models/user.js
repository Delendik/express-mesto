const { Schema, model } = require('mongoose');

const userSchema = new Schema({
  name: {
    type: String,
    required: true,
    minlength: 2,
    maxlength: 30,
  },
  about: {
    type: String,
    required: true,
    minlength: 2,
    maxlength: 30,
  },
  avatar: {
    type: String,
    required: true,
    validate: {
      validator(v) {
        return /^(http|https):\/\/w?w?w?\.?(\w|\D)+\.(ru|com)/.test(v);
      },
    },
  },
});

module.exports = model('user', userSchema);
